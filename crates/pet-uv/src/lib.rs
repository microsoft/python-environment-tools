    #[test]
    fn test_try_from_managed_install_identifies_uv_python() {
        use pet_core::env::PythonEnv;

        let temp_dir = TempDir::new().unwrap();
        // Normalize install_dir the same way get_uv_python_install_dir does,
        // so the path casing matches the norm_case applied by PythonEnv::new().
        let install_dir = norm_case(temp_dir.path());

        // Create a uv-managed Python install
        let version_dir = install_dir.join("cpython-3.12.3-linux-x86_64-gnu");
        let executable = if cfg!(windows) {
            std::fs::create_dir_all(&version_dir).unwrap();
            let exe = version_dir.join("python.exe");
            std::fs::File::create(&exe).unwrap();
            exe
        } else {
            let bin_dir = version_dir.join("bin");
            std::fs::create_dir_all(&bin_dir).unwrap();
            let exe = bin_dir.join("python");
            std::fs::File::create(&exe).unwrap();
            exe
        };

        let uv = Uv {
            workspace_directories: Arc::new(Mutex::new(Vec::new())),
            uv_install_dir: Some(install_dir),
        };

        let python_env = PythonEnv::new(executable, Some(version_dir.clone()), None);
        let result = uv.try_from_managed_install(&python_env);
        assert!(
            result.is_some(),
            "Should identify uv-managed Python install"
        );
        let env = result.unwrap();
        assert_eq!(env.kind, Some(PythonEnvironmentKind::Uv));
        assert_eq!(env.version, Some("3.12.3".to_string()));
    }

